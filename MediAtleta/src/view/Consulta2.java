    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
                CadastroDao dao = new CadastroDao();
        boolean status = dao.conectar();
        if (status == true) {
            Jogador jogador = dao.consultar(txtNome.getText(), txtNumCamisa.getText());
            if (jogador==null) {
                JOptionPane.showMessageDialog(null, "O jogador não está cadastrado!");
            } else {
                txtRitmo.setText(String.valueOf(jogador.getRitmo()));
                txtFinalizacao.setText(String.valueOf(jogador.getFinalizacao()));
                txtDefesa.setText(String.valueOf(jogador.getDefesa()));
                txtPasse.setText(String.valueOf(jogador.getPasse()));
                txtDrible.setText(String.valueOf(jogador.getDrible()));
                txtCobrFalta1.setText(String.valueOf(jogador.getCobrFalta()));
                txtCobrPenalti1.setText(String.valueOf(jogador.getCobrPenalti()));    
                txtPosicao.setText(jogador.getPosicao());
                btnExcluir.setEnabled(true);
                
            }

        } else {
            JOptionPane.showMessageDialog(null, "Erro na conexão");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

