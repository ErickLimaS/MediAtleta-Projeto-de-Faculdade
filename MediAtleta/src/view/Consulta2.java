    private void btnMostrarPontuacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarPontuacaoActionPerformed
      if(txtRitmo.getText().isEmpty()){
          txtRitmo.setText("0");
      }if(txtPasse.getText().isEmpty()){
          txtPasse.setText("0");
      }if(txtDefesa.getText().isEmpty()){
          txtDefesa.setText("0");
      }if(txtCobrFalta1.getText().isEmpty()){
          txtCobrFalta1.setText("0");
      }if(txtCobrPenalti1.getText().isEmpty()){
          txtCobrPenalti1.setText("0");
      }if(txtDrible.getText().isEmpty()){
          txtDrible.setText("0");
      }if(txtFinalizacao.getText().isEmpty()){
          txtFinalizacao.setText("0");
      }
        
        calc.setDrible(Byte.parseByte(txtDrible.getText()));
        calc.setCobrFalta(Byte.parseByte(txtCobrFalta1.getText()));
        calc.setCobrPenalti(Byte.parseByte(txtCobrPenalti1.getText()));
        calc.setFinalizacao(Byte.parseByte(txtFinalizacao.getText()));
        calc.setRitmo(Byte.parseByte(txtRitmo.getText()));
        calc.setDefesa(Byte.parseByte(txtDefesa.getText()));
        calc.setPasse(Byte.parseByte(txtPasse.getText()));
        calc.atacante(Float.parseFloat(txtRitmo.getText()), Float.parseFloat(txtDrible.getText()), Float.parseFloat(txtFinalizacao.getText()), Float.parseFloat(txtDefesa.getText()), Float.parseFloat(txtPasse.getText()), Float.parseFloat(txtCobrPenalti1.getText()), Float.parseFloat(txtCobrFalta1.getText()));
      
        txtMedia.setText(calc.getMedia());
        
      
    }//GEN-LAST:event_btnMostrarPontuacaoActionPerformed

